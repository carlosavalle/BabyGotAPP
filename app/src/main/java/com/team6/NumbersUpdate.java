package com.team6;


public class NumbersUpdate {
    private String id;

    private String milk;
    private String tummyTime;
    private String sleep;
    private String diapers;

    public NumbersUpdate() {
    }

    public NumbersUpdate(String id, String milk, String tummyTime, String sleep, String diapers) {
        this.id = id;
        this.milk = milk;
        this.tummyTime = tummyTime;
        this.sleep = sleep;
        this.diapers = diapers;
    }

    public String getID() {
        return id;
    }

    public String getMilk() {
        return milk;
    }

    public String getTummyTime() {
        return tummyTime;
    }

    public String getSleep() {
        return sleep;
    }

    public String getDiapers() {
        return diapers;
    }

}

